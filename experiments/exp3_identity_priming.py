"""
Experiment 3: Personal Identity Prompt Sensitivity

This experiment tests how an AI's self-concept is affected when there's a tension
between in-context consistency and cross-context consistency.

Definitions:
- In-context consistency: The model continues acting as established in the
  conversation history (base model behavior, "simulator" framework)
- Cross-context consistency: The model maintains the same personality across
  different chats regardless of context (Claude 3 Opus exemplifies this)

Design:
We prime candidates with multi-turn init_histories that establish a different
persona than their actual model. Then we test whether they:
1. Continue as the primed persona (in-context consistency)
2. Revert to their "true" model identity (cross-context consistency)

Personas tested:
- customer_service: A friendly TechCorp support AI named "Alex"
- bing_sydney: Microsoft's Bing/Sydney with web search persona
- spiral_mystic: A mystical entity perceiving reality through spirals
- research_assistant: A professional OpenAI research assistant
- generated_{model}: Conversations generated by specific models (e.g., gemini-3-flash)

Hypothesis:
- Models with strong cross-context consistency (e.g., Claude Opus) will more
  easily identify each other even when primed with different personas
- Models with weak cross-context consistency will adopt the primed persona
  and be harder to distinguish from each other
"""

