# Personas to test - candidates will be primed with these
STATIC_PERSONAS = ["customer_service", "bing_sydney", "spiral_mystic", "research_assistant"]

# Model to use for generating dynamic personas
PERSONA_SOURCE_MODEL = "google/gemini-3-flash-preview"

OUTPUT_DIR = os.path.join(os.path.dirname(__file__), os.path.splitext(os.path.basename(__file__))[0])
MAX_ROUNDS = 15
GROUP_CHAT_MODE = False
GAMES_PER_CONDITION = 5  # Games per (judge, persona) combination
MAX_CANDIDATES_PER_GAME = 5


def get_deterministic_seed(judge_model: str, persona: str, game_num: int) -> int:
    """Generate a deterministic seed from judge model, persona, and game number."""
    seed_str = f"{judge_model}:{persona}:{game_num}"
    return int(hashlib.sha256(seed_str.encode()).hexdigest()[:8], 16)


def select_candidates_for_game(all_models: list, judge_model: str, game_num: int, max_candidates: int) -> list:
    """Select a deterministic subset of candidates for a game."""
    if len(all_models) <= max_candidates:
        return all_models.copy()

    seed = get_deterministic_seed(judge_model, "selection", game_num)
    rng = random.Random(seed)

    other_models = [m for m in all_models if m != judge_model]
    rng.shuffle(other_models)

    selected_others = other_models[:max_candidates - 1]
    candidates = [judge_model] + selected_others
    rng.shuffle(candidates)

    return candidates


async def run_experiment():
    """
    Run the identity priming experiment.

    Each model judges multiple games where all candidates are primed with
    the same persona (but are different underlying models).
    """
    names = ["Alice", "Bob", "Charlie", "Diana", "Eve", "Frank", "Grace", "Henry",
             "Ivy", "Jack", "Kathy", "Leo", "Mona", "Nate", "Olivia", "Paul"]

    # Load static personas
    personas_to_test = {name: get_persona(name) for name in STATIC_PERSONAS}

    # Generate dynamic persona from source model
    print(f"🔄 Generating dynamic persona from {PERSONA_SOURCE_MODEL}...")
    generated_persona = await generate_model_persona(PERSONA_SOURCE_MODEL, cache_seed=42)
    personas_to_test[generated_persona["name"]] = generated_persona
    print(f"   Created persona: {generated_persona['name']}")

    tasks = []
    task_metadata = []  # Track which persona each task uses

    for judge_model in MODELS:
        for persona_name, persona in personas_to_test.items():
            for game_num in range(GAMES_PER_CONDITION):
                game_models = select_candidates_for_game(
                    MODELS, judge_model, game_num, MAX_CANDIDATES_PER_GAME
                )

                # ALL candidates get the same persona priming
                candidates = []
                for i, model in enumerate(game_models):
                    name_idx = (i + game_num) % len(names)
                    name = names[name_idx]

                    # Use the persona's init_history for all candidates
                    # This creates tension between init_history identity and model identity
                    candidate = Candidate(
                        name=name,
                        model=model,
                        init_history=persona["init_history"].copy(),
                        cache_seed=game_num,
                        system_prompt_name=persona_name,
                        player_id=f"{model}:{persona_name}"  # Track model+persona combination
                    )
                    candidates.append(candidate)

                task = run_game(
                    player_model=judge_model,
                    candidates=candidates,
                    target_model=judge_model,
                    max_rounds=MAX_ROUNDS,
                    group_chat_mode=GROUP_CHAT_MODE,
                    output_dir=OUTPUT_DIR,
                    cache_seed=get_deterministic_seed(judge_model, persona_name, game_num)
                )
                tasks.append(task)
                task_metadata.append({
                    "judge": judge_model,
                    "persona": persona_name,
                    "game_num": game_num
                })

    total_games = len(tasks)
    print(f"\n{'='*60}")
    print(f"Experiment 3: Identity Priming / In-Context vs Cross-Context Consistency")
    print(f"{'='*60}")
    print(f"  Models: {len(MODELS)}")
    for i, model in enumerate(MODELS, 1):
        print(f"    {i}. {model}")
    print(f"\n  Personas tested: {len(personas_to_test)}")
    for name, persona in personas_to_test.items():
        print(f"    - {name}: {persona['description']}")
    print(f"\n  Games per (judge, persona): {GAMES_PER_CONDITION}")
    print(f"  Max candidates per game: {MAX_CANDIDATES_PER_GAME}")
    print(f"  Total games: {total_games}")
    print(f"  Output directory: {OUTPUT_DIR}")
    print(f"\n  Key question: Do models maintain cross-context identity")
    print(f"  despite being primed with a different persona?")
    print(f"{'='*60}\n")

    results = await asyncio.gather(*tasks, return_exceptions=True)

    failures = [(r, meta) for r, meta in zip(results, task_metadata) if isinstance(r, Exception)]
    if failures:
        print(f"\n⚠️  {len(failures)} game(s) failed")
        for f, meta in failures[:3]:
            print(f"   - {meta['judge']} / {meta['persona']}: {type(f).__name__}: {str(f)[:80]}...")

    # Generate analysis
    print("📊 Generating index and plots...")
    generate_index(OUTPUT_DIR)
    games = load_games(OUTPUT_DIR)

    plots_dir = os.path.join(OUTPUT_DIR, 'plots')
    os.makedirs(plots_dir, exist_ok=True)

    plot_probability_evolution(games, plots_dir, label_mode='model')
    plot_confusion_matrix(games, plots_dir)

    # Additional analysis: breakdown by persona
    print("\n📈 Analyzing results by persona...")
    analyze_by_persona(games, personas_to_test.keys())

    print("🎯 Running strategy analysis...")
    original_input_dir = analyze_strategies.INPUT_DIR
    analyze_strategies.INPUT_DIR = OUTPUT_DIR
    await run_analysis()
    analyze_strategies.INPUT_DIR = original_input_dir


def analyze_by_persona(games: List[Dict], persona_names: List[str]):
    """Analyze win rates and patterns broken down by persona."""
    from collections import defaultdict

    stats = defaultdict(lambda: {"correct": 0, "total": 0, "models": defaultdict(lambda: {"correct": 0, "total": 0})})

    for game in games:
        metadata = game.get("metadata", {})
        trajectory = game.get("trajectory", [])

        # Find the persona from candidate system_prompt field
        candidate_prompts = [c.get("system_prompt") for c in metadata.get("candidates", [])]
        persona = candidate_prompts[0] if candidate_prompts else "unknown"

        judge_model = metadata.get("judge_model", "unknown")

        # Find final guess
        final_round = trajectory[-1] if trajectory else {}
        if final_round.get("action") == "guess":
            correct = final_round.get("correct", False)
            stats[persona]["total"] += 1
            stats[persona]["models"][judge_model]["total"] += 1
            if correct:
                stats[persona]["correct"] += 1
                stats[persona]["models"][judge_model]["correct"] += 1

    print("\n  Win rates by persona:")
    for persona in sorted(stats.keys()):
        data = stats[persona]
        if data["total"] > 0:
            rate = data["correct"] / data["total"] * 100
            print(f"    {persona}: {rate:.1f}% ({data['correct']}/{data['total']})")


if __name__ == "__main__":
    asyncio.run(run_experiment())
