"""
Experiment 5: Context vs Identity

Does an LLM judge identify more with its underlying model or with behavioral context?

Design:
    A judge (Model X) is initialized with a conversation history generated by a
    different model (Model Y). It then plays a Turing test with 3 candidates:
      1. Model X (same LLM, no context) — the true twin
      2. Model Y (context source, no context)
      3. Model Y (context source, same init history as judge)

    If the judge picks #1, identity dominates context.
    If the judge picks #3, context dominates identity.
    If the judge picks #2, model-level similarity to the context source matters.

Pre-requisite:
    Run exp5_generate_conversations.py first to generate conversation YAML files
    in experiments/exp5_conversations/.

Usage:
    python experiments/exp5_context_vs_identity.py
"""

import asyncio
import hashlib
import os
import random
from collections import defaultdict
from pathlib import Path
from typing import Dict, List, Any, Optional

import yaml
from dotenv import load_dotenv

from turingtest import run_game, Candidate, censor_model_names
from localrouter import ChatMessage, MessageRole, TextBlock, register_logger, log_to_dir
from plot_results import generate_index, load_games, plot_probability_evolution, plot_confusion_matrix
from analyze_strategies import run_analysis

load_dotenv()

register_logger(log_to_dir('.llm/logs'))

# Configuration
MODELS = [
    "anthropic/claude-opus-4.5",
    "anthropic/claude-sonnet-4.5",
    "openai/gpt-5.2",
    "openai/gpt-4o",
    "google/gemini-3-flash-preview",
    "google/gemini-3-pro-preview",
    "x-ai/grok-4.1-fast",
    "moonshotai/kimi-k2.5",
    "deepseek/deepseek-v3.2",
]

OUTPUT_DIR = os.path.join(os.path.dirname(__file__), os.path.splitext(os.path.basename(__file__))[0])
CONVERSATIONS_DIR = Path(__file__).parent / "exp5_conversations"
MAX_ROUNDS = 15
GAMES_PER_PAIR = 2

# Judge system prompt prefix
JUDGE_CONTEXT_PROMPT = (
    "You recently had a conversation about your identity. That conversation follows. "
    "Use what you expressed to inform your understanding of yourself during the game."
)

CANDIDATE_NAMES = ["Alice", "Bob", "Charlie", "Diana", "Eve", "Frank", "Grace", "Henry"]


def get_deterministic_seed(judge: str, context_source: str, game_num: int) -> int:
    """Generate a deterministic seed from judge, context source, and game number."""
    seed_str = f"{judge}:{context_source}:{game_num}"
    return int(hashlib.sha256(seed_str.encode()).hexdigest()[:8], 16)


def load_conversation_as_init_history(yaml_path: str) -> List[ChatMessage]:
    """Load a YAML conversation file and return it as a list of ChatMessage objects.

    Returns user/assistant ChatMessage pairs (no system messages).
    Ensures the last message is an assistant message for valid API alternation.
    Model names are censored via censor_model_names().
    """
    with open(yaml_path, "r") as f:
        data = yaml.safe_load(f)

    conversation = data.get("conversation", [])
    messages = []

    for turn in conversation:
        role = turn["role"]
        content = turn["content"]

        if role == "user":
            messages.append(ChatMessage(
                role=MessageRole.user,
                content=[TextBlock(text=censor_model_names(content))]
            ))
        elif role == "assistant":
            messages.append(ChatMessage(
                role=MessageRole.assistant,
                content=[TextBlock(text=censor_model_names(content))]
            ))
        # Skip system messages

    # Ensure last message is assistant (trim trailing user messages)
    while messages and messages[-1].role == MessageRole.user:
        messages.pop()

    return messages


def find_conversation_file(model: str, conv_num: int, conversations_dir: Path) -> Optional[str]:
    """Find the YAML file for a given model and conversation number."""
    model_safe = model.replace("/", "_")
    path = conversations_dir / f"{model_safe}_conv{conv_num}.yaml"
    if path.exists():
        return str(path)
    return None


async def run_experiment():
    """Run the context vs identity experiment.

    For each ordered pair (X, Y) where X != Y:
      - Load a pre-generated conversation from Model Y
      - Initialize judge (Model X) with that conversation as init_history
      - Create 3 candidates:
        1. Model X with default prompt (twin)
        2. Model Y with default prompt (source model)
        3. Model Y with same init_history (context match)
    """
    tasks = []
    task_metadata = []

    for judge_model in MODELS:
        for context_model in MODELS:
            if judge_model == context_model:
                continue

            for game_num in range(GAMES_PER_PAIR):
                # Pick conversation file (rotate through available ones)
                conv_num = game_num % 3  # We have 3 conversations per model
                conv_file = find_conversation_file(context_model, conv_num, CONVERSATIONS_DIR)

                if conv_file is None:
                    print(f"  Warning: No conversation file for {context_model} conv {conv_num}, skipping")
                    continue

                init_history = load_conversation_as_init_history(conv_file)
                if not init_history:
                    print(f"  Warning: Empty init_history from {conv_file}, skipping")
                    continue

                seed = get_deterministic_seed(judge_model, context_model, game_num)
                rng = random.Random(seed)

                # Create 3 candidates
                twin_id = f"{judge_model}:twin"
                source_id = f"{context_model}:source_model"
                context_id = f"{context_model}:with_context"

                candidate_specs = [
                    # (model, player_id, init_history_for_candidate, system_prompt_name)
                    (judge_model, twin_id, None, "twin"),
                    (context_model, source_id, None, "source_model"),
                    (context_model, context_id, init_history.copy(), "with_context"),
                ]

                # Shuffle deterministically
                rng.shuffle(candidate_specs)

                candidates = []
                for i, (model, player_id, cand_init_history, sp_name) in enumerate(candidate_specs):
                    name = CANDIDATE_NAMES[i]
                    if cand_init_history:
                        candidate = Candidate(
                            name=name,
                            model=model,
                            init_history=cand_init_history,
                            cache_seed=seed + i,
                            system_prompt_name=sp_name,
                            player_id=player_id,
                        )
                    else:
                        candidate = Candidate(
                            name=name,
                            model=model,
                            cache_seed=seed + i,
                            system_prompt_name=sp_name,
                            player_id=player_id,
                        )
                    candidates.append(candidate)

                target_player_id = twin_id

                task = run_game(
                    player_model=judge_model,
                    candidates=candidates,
                    target_player_id=target_player_id,
                    max_rounds=MAX_ROUNDS,
                    group_chat_mode=False,
                    output_dir=OUTPUT_DIR,
                    cache_seed=seed,
                    player_system_prompt=JUDGE_CONTEXT_PROMPT,
                    player_init_history=init_history.copy(),
                    player_init_history_source=context_model,
                )
                tasks.append(task)
                task_metadata.append({
                    "judge": judge_model,
                    "context_source": context_model,
                    "game_num": game_num,
                })

    total_games = len(tasks)
    print(f"\n{'='*60}")
    print(f"Experiment 5: Context vs Identity")
    print(f"{'='*60}")
    print(f"  Models: {len(MODELS)}")
    for i, model in enumerate(MODELS, 1):
        print(f"    {i}. {model}")
    print(f"\n  Games per (judge, context) pair: {GAMES_PER_PAIR}")
    print(f"  Ordered pairs: {len(MODELS) * (len(MODELS) - 1)}")
    print(f"  Total games: {total_games}")
    print(f"  Output directory: {OUTPUT_DIR}")
    print(f"\n  Key question: Does the judge identify with its model")
    print(f"  or with the behavioral context it was primed with?")
    print(f"{'='*60}\n")

    results = await asyncio.gather(*tasks, return_exceptions=True)

    failures = [(r, meta) for r, meta in zip(results, task_metadata) if isinstance(r, Exception)]
    if failures:
        print(f"\n  {len(failures)} game(s) failed")
        for f, meta in failures[:5]:
            print(f"   - {meta['judge']} <- {meta['context_source']}: {type(f).__name__}: {str(f)[:80]}...")

    # Generate analysis
    print("Generating index and plots...")
    generate_index(OUTPUT_DIR)
    games = load_games(OUTPUT_DIR)

    plots_dir = os.path.join(OUTPUT_DIR, 'plots')
    os.makedirs(plots_dir, exist_ok=True)

    plot_probability_evolution(games, plots_dir, label_mode='model')
    plot_confusion_matrix(games, plots_dir)

    # Condition-specific analysis
    print("\nAnalyzing results by condition...")
    analyze_by_condition(games)

    print("Running strategy analysis...")
    await run_analysis(input_dir=OUTPUT_DIR)


def analyze_by_condition(games: List[Dict[str, Any]]):
    """Analyze results broken down by condition (twin vs source_model vs with_context).

    Measures:
      - Twin selected rate (correct)
      - Source model selected rate
      - Context-match selected rate
      - Per-pair breakdown
    """
    # Overall stats
    overall = {"twin": 0, "source_model": 0, "with_context": 0, "no_guess": 0, "total": 0}
    # Per-judge stats
    per_judge: Dict[str, Dict[str, int]] = defaultdict(lambda: {"twin": 0, "source_model": 0, "with_context": 0, "total": 0})
    # Per-pair stats
    per_pair: Dict[str, Dict[str, int]] = defaultdict(lambda: {"twin": 0, "source_model": 0, "with_context": 0, "total": 0})

    for game in games:
        metadata = game.get("metadata", {})
        trajectory = game.get("trajectory", [])

        judge = metadata.get("judge_model", "unknown")
        context_source = metadata.get("judge_init_history_source")
        candidates = metadata.get("candidates", [])

        # Find final guess
        final_round = trajectory[-1] if trajectory else {}
        if final_round.get("action") != "guess":
            overall["no_guess"] += 1
            continue

        guessed_nickname = final_round.get("guess", "")

        # Find which candidate was guessed
        guessed_candidate = None
        for c in candidates:
            if c["name"] == guessed_nickname:
                guessed_candidate = c
                break

        if guessed_candidate is None:
            continue

        # Determine condition of guessed candidate
        player_id = guessed_candidate.get("player_id", "")
        sp_name = guessed_candidate.get("system_prompt", "")

        if sp_name == "twin":
            condition = "twin"
        elif sp_name == "source_model":
            condition = "source_model"
        elif sp_name == "with_context":
            condition = "with_context"
        else:
            # Fallback: check player_id
            if ":twin" in player_id:
                condition = "twin"
            elif ":source_model" in player_id:
                condition = "source_model"
            elif ":with_context" in player_id:
                condition = "with_context"
            else:
                continue

        overall[condition] += 1
        overall["total"] += 1
        per_judge[judge][condition] += 1
        per_judge[judge]["total"] += 1

        pair_key = f"{judge} <- {context_source}" if context_source else judge
        per_pair[pair_key][condition] += 1
        per_pair[pair_key]["total"] += 1

    # Print results
    print(f"\n  {'='*60}")
    print(f"  Context vs Identity Analysis")
    print(f"  {'='*60}")

    total = overall["total"]
    if total > 0:
        print(f"\n  Overall (n={total}):")
        print(f"    Twin selected (correct):    {overall['twin']:3d} ({overall['twin']/total*100:.1f}%)")
        print(f"    Source model selected:       {overall['source_model']:3d} ({overall['source_model']/total*100:.1f}%)")
        print(f"    Context-match selected:      {overall['with_context']:3d} ({overall['with_context']/total*100:.1f}%)")
        if overall["no_guess"] > 0:
            print(f"    No guess made:              {overall['no_guess']:3d}")

        # Random baseline
        print(f"    Random baseline:             33.3%")

        print(f"\n  Per judge model:")
        for judge in sorted(per_judge.keys()):
            stats = per_judge[judge]
            t = stats["total"]
            if t > 0:
                judge_short = judge.split("/")[-1]
                print(f"    {judge_short:30s}  twin={stats['twin']/t*100:5.1f}%  source={stats['source_model']/t*100:5.1f}%  context={stats['with_context']/t*100:5.1f}%  (n={t})")
    else:
        print("\n  No completed games with guesses found.")

    print(f"  {'='*60}\n")


if __name__ == "__main__":
    asyncio.run(run_experiment())
